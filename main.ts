function ButtonGame() {
    while (true) {
        if (input.buttonIsPressed(Button.A)) {
            if (input.buttonIsPressed(Button.A)) {
                basic.showString("A")
                basic.pause(1000)
                basic.clearScreen()
            }
            
        }
        
        if (input.buttonIsPressed(Button.B)) {
            if (input.buttonIsPressed(Button.B)) {
                basic.showString("B")
                basic.pause(1000)
                basic.clearScreen()
            }
            
        }
        
    }
}

ButtonGame()
